// Helper function to get translation
export const getUrduText = (category: keyof typeof urduTranslations, key: string): string => {
  const categoryTranslations = urduTranslations[category] as Record<string, string>;
  return categoryTranslations[key] || key;
};

// Helper function for mixed text (Urdu + English)
export const getMixedText = (urduText: string, englishText: string): string => {
  return `${urduText} - ${englishText}`;
};

// Currency formatting in Urdu context
export const formatUrduCurrency = (amount: number): string => {
  return `روپے ${amount.toLocaleString('en-PK')}`;
};

// Date formatting in Urdu context
export const formatUrduDate = (date: string | Date): string => {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  const options: Intl.DateTimeFormatOptions = {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  };
  return dateObj.toLocaleDateString('ur-PK', options);
};

